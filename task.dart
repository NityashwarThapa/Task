// This is a simple Dart program

void main() {
  // Print a greeting
  print('Hello, World!');

  // Declare two numbers
  int num1 = 10;
  int num2 = 20;

  // Calculate their sum
  int sum = num1 + num2;

  // Print the result
  print('The sum of $num1 and $num2 is $sum');

  // Declare two numbers
  int a = 5;
  int b = 3;
  print(a + b);

  var c = 20;
  var d = 10;

  int product = c * d;
  print('The product of $c and $d is $product');
}
